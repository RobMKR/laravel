            <b>9.14</b> Սույն Կանոնները տարբեր մեկնաբանությունների, ցանկացած վիճելի հարցերի առաջացման և (կամ) սույն Կանոններով չկարգավորվող այլ հարցերի ծագման դեպքում վերջնական որոշումը կայացնում է Միջոցառման Կազմակերպիչը՝ իր հայեցողությամբ:<br>
