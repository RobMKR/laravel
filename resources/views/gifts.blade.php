            <b>8.1.1</b> Թեժ Գծի օպերատորի աշխատանքի պայմաններն ու կանոնները սահմանում և վերահսկում է Միջոցառման Կազմակերպիչը: Թեժ Գծի հետ կապ հնարավոր է հաստատել հեռախոսազանգի միջոցով, նշված համարին հեռախոսազանգի արժեքը սահմանվում է հեռախոսային կապի այն օպերատորի կողմից սահմանված սակագների համաձայն, որի ծառայություններից հետաքրքրված անձը, ներառյալ Միջոցառման Մասնակիցը, օգտվում է հեռախոսազանգն իրականացնելու համար: Հեռախոսազանգի արժեքը և հեռախոսազանգի հետ կապված այլ ծախսեր Միջոցառման Կազմակերպչի կողմից փոխհատուցման ենթակա չեն և դրա հետ կապված որևէ պահանջ չի կարող ներկայացվել Միջոցառման Կազմակերպչին:<br>
            <b>8.1.2</b> Թեժ Գիծը հասանելի է սույն Կանոններով սահմանված Միջոցառման անցկացման ողջ ընթացքում՝ երկուշաբթիից ուրբաթ օրերին, ժամը 9:30-ից 18:30-ը: <br>
            <b>8.1.3</b> Թեժ Գծին ուղղված հեռախոսազանգերի քանակից կախված հնարավոր է, որպեսզի անհրաժեշտ լինի, որ Թեժ Գծի հետ կապ հաստատել ցանկացող անձը սպասի որոշակի ժամանակ կամ կրկին փորձի զանգահարել, որը հանդիսանում է բնականոն գործընթաց և որի համար Միջոցառման Կազմակերպիչը չի կրում որևէ պատասխանատվություն: <br>


