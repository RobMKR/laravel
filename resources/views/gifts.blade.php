        </p>

        <p>
            <span><b>4. ՄԻՋՈՑԱՌՄԱՆԸ ՄԱՍՆԱԿՑՈՒԹՅՈՒՆԸ ԵՎ ՄԻՋՈՑԱՌՄԱՆ ՄԱՍՆԱԿՑԻ (ՄԱՍՆԱԿԻՑՆԵՐԻ) ԳՐԱՆՑՈՒՄ </b></span><br>


        </p>
        <p>
            <span><b>4.1. ՄԱՍ 1. Սույն Կանոններով սահմանված գործողությունների կատարում՝ Միջոցառման շրջանակներում համապատասխան Նվեր ստանալու հնարավորություն ձեռք բերելու համար.  </b></span><br>
            Միջոցառմանը մասնակցելու ցանկություն ունեցող անձն իրականացնում է հետևյալ գործողությունները. 
ներկայանում է իր համար նախընտրելի Միջոցառման Կենտրոն՝ դրա աշխատանքային ժամերին,
Միջոցառման ժամկետում ներառված համապատասխան օրացուցային շաբաթվա ընթացքում Միջոցառման Կենտրոններից ձեռք է բերում Ծխախոտային Ապրանքների առնվազն 7 Տուփ, ,
տվյալ Տուփերի ձեռք բերման Կտրոնների վրա գրում է իր Հեռախոսահամարը և անուն, ազգանունը (անձը տրամադրում է իր Հեռախոսահամարը և նշված տվյալները Միջոցառման ընթացքում իր մասնակցության մասին տեղեկություններ ստանալու, ներառյալ՝ իր կուտակած Միավորների մասին տեղեկություններ ստանալու նպատակով (Հեռախոսահամարն օգտագործվում է նաև Միջոցառման շրջանակներում համապատասխան անձի/Մասնակցի նույնականացման նպատակով),
տվյալ Տուփերի Փայլաթիթեղները դրանց Կտրոնների հետ ամրակցված ներկայացնում է Միջոցառման Կազմակերպչին՝ գցելով դրանք Միջոցառման Կենտրոնում տեղադրված համապատասխան արկղի մեջ,
շաբաթական կուտակում է առնվազն 7 Միավոր, որի արդյունքում ձեռք է բերում Միջոցառման համապատասխան Նվեր ստանալու հնարավորություն, 
բավարար Միավորներ կուտակելու և մյուս պայմանների համապատասխանության դեպքում սույն Կանոններով սահմանված կարգով տեղեկացվում է որպես Միջոցառման Մասնակից գրանցվելու և/կամ Նվեր ստանալու ժամանակի և վայրի մասին:


        </p>
        <p>
            <span><b>4.1. ՄԱՍ 2. Սույն Կանոններով սահմանված կարգով գրանցում որպես Միջոցառման Մասնակից և համապատասխան Նվերի ստացում: </b></span><br>
            Որպես Միջոցառման Մասնակից գրանցվելու համար վերոնշյալ պահանջները բավարարող անձը, ով սույն Կանոններով սահմանված կարգով իրավասու է մասնակցել Միջոցառմանը, եթե սույն Կանոնների 4.1. կետով սահմանված բոլոր գործողությունների կատարման արդյունքում տեղեկացվում է, որ պետք է ներկայանա Միջոցառման համապատասխան Կենտրոն որպես Միջոցառման Մասնակից գրանցվելու համար, իրականացնում է հետևյալ գործողությունները. 
համապատասխան ժամին, որի մասին նախապես տեղեկացվել է, ներկայանում է համապատասխան Միջոցառման Կենտրոն,  
տեղեկացնում է Միջոցառման Պրոմոութերին՝ որպես Միջոցառման Մասնակից գրանցվելու իր ցանկության մասին, և հայտնում է իր տվյալները՝ անհատականացման և գրանցման նպատակով, 
ներկայացնում և տրամադրում է Միջոցառման Պրոմոութերին իր՝ անձը հաստատող փաստաթուղթը և Հեռախոսահամար, 
հավաստում է և անհրաժեշտության դեպքում ստորագրում է համապատասխան փաստաթղթեր, առ այն, որ ինքը հանդիսանում է ծխախոտային արտադրանքի սպառող և չի պատկանում այն անձանց շրջանակին, ովքեր իրավասու չեն մասնակցելու Միջոցառմանը, ինչպես սահմանված է սույն Կանոններով, 
ծանոթանում է Միջոցառման Կանոններին, ինչպես նաև գրանցման կանոններին և հավաստում է, որ ընդունում է դրանք ամբողջությամբ՝ մինչև գրանցվելը որպես Միջոցառման Մասնակից: 
            

        </p>

        <p>
            <span><b>4.2.1 Միջոցառման Պրոմոութերը  </b></span> 
             Միջոցառման Մասնակցին գրանցելու համար իրականացնում է հետևյալ գործողությունները. 
Ստուգում է ներկայացված անձը հաստատող փաստաթուղթը, հավաստիանալով որ համապատասխան անձը հանդիսանում է Հայաստանի Հանրապետության քաղաքացի, լրացել է նրա 18 (տասնութ) տարին, և այդ մասով իրավասու է մասնակցելու Միջոցառմանը, 
Հավաստիանում է և ըստ անհրաժեշտության տրամադրում է համապատասխան փաստաթղթեր Միջոցառման Մասնակցի կողմից ստորագրման և/կամ հաստատման, առ այն, որ որպես Միջոցառման Մասնակից գրանցվել ցանկացող անձը հանդիսանում է ծխախոտային արտադրանքի սպառող և չի պատկանում այն անձանց շրջանակին, ովքեր իրավասու չեն մասնակցելու Միջոցառմանը, ինչպես սահմանված է սույն Կանոններով, 
Վերոնշյալ բոլոր պայմանների համապատասխանության և բավարար լինելու դեպքում միայն, գրանցում է հետաքրքրված անձին որպես Միջոցառման Մասնակից, որի արդյունքում ստեղծվում է տվյալ անձի Անձնական Պրոֆիլը: 
            

        </p>

        <p>
            <span><b>4.3 Որպես Միջոցառման Մասնակից գրանցվելով՝ անձը հավաստում է, որ հանդիսանում է ծխախոտային արտադրանքի սպառող հանդիսացող Հայաստանի Հանրապետության քաղաքացի, ում 18 (տասնութ) տարին լրացել է գրանցման պահի դրությամբ, ծանոթացել է, ընդունում է և համաձայն է սույն Կանոններին: </b></span>
        </p>
        <p>
            <span><b>4.4 Անձի գրանցումը որպես Միջոցառման Մասնակից պարտադիր է մինչև Միջոցառման շրջանակներում համապատասխան Նվերը ստանալը: </b></span>
        </p>
        <p>
            <span><b>4.5 Հատուկ պայմաններ, որոնք վերաբերում են որպես Միջոցառման Մասնակից գրանցմանը. </b></span>
        </p>

        <p>
            4.5.1 Հետաքրքրված անձն անձամբ է պատասխանատու որպես Միջոցառման Մասնակից գրանցվելու ընթացքում, ինչպես նաև Միջոցառման ողջ ընթացքում իր կողմից ներկայացված տեղեկատվության հավաստիության, իրավաչափության և ճշգրտության համար: Որպես Միջոցառման Մասնակից գրանցվելու դեպքում տվյալ անձի կողմից տրամադրված տվյալների անճշտության և/կամ անհամապատասխանության համար ամբողջությամբ պատասխանատվություն է կրում տվյալ անձը: 
        </p>

        <p>
            4.5.2 Գրանցվելով որպես Միջոցառման Մասնակից հետաքրքված անձը հաստատում է իր անձնական տվյալների ճշգրտությունը, ներառյալ անունը, ազգանունը, անձը հաստատող փաստաթղթի համարը և Հեռախոսահամարը (իր ընտրությամբ՝ ծննդյան ամսաթիվը, էլեկտրոնային փոստի վերաբերյալ կամ ցանկացած այլ տվյալներ), որոնք լրացվում են Միջոցառման Մասնակցի կողմից կամ տրամադրվում են լրացման համար վերջինիս կողմից: Միջոցառման Մասնակիցն իրավասու չէ տրամադրել իր Անձնական Պրոֆիլի մասին ցանկացած տեղեկություն ցանկացած երրորդ անձի: 
        </p>
        <p>
            4.5.3 Գրանցվելով որպես Միջոցառման Մասնակից Միջոցառման Կենտրոնում տվյալ անձի համար ձևավորվում է Անձնական Պրոֆիլ, որը, ներառյալ դրա վերաբերյալ տեղեկությունները, հասանելի են Միջոցառման տվյալների բազայում: Միջոցառման Կազմակերպիչը հնարավորություն ունի ստանալու Միջոցառման Մասնակիցների կողմից տրամադրված և գրանցված տեղեկությունները և օգտագործել դրանք սույն Կանոններով սահմանված կարգով և պայմաններով: 
        </p>

        <p>
            4.5.4 Որպես Միջոցառման Մասնակից գրանցվելն անվճար է: Անձի գրանցումը որպես Միջոցառման Մասնակից հնարավորություն է տալիս վերջինիս մասնակցելու Միջոցառմանը և չի առաջացնում Միջոցառման Կազմակերպչի համար որևէ հավելյալ պարտավորություն:  
        </p>

        <p>
            4.5.5 Միջոցառման Մասնակցի գրանցման վերաբերյալ հավելյալ տեխնիկական առանձնահատկությունները սահմանվում են Միջոցառման Կազմակերպչի կողմից, որի վերաբերյալ տեղեկատվությունը հասանելի կլինի որպես Միջոցառման Մասնակից անձի գրանցվելու պահին: 
        </p>
        <p>
