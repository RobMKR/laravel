«ՋԵՅ ԹԻ ԱՅ ԱՐՄԵՆԻԱ» ՓԲԸ-ի և «ՋԵՅ ԹԻ ԻՆԹԵՐՆԵՅՇՆԼ ԼՅՈՒՔՍԵՄԲՈՒՐԳ ԷՍ. ԷՅ» ընկերության հայաստանյան ներկայացուցչության աշխատակիցները
«Յունիքորն Թրեյդ» ՓԲԸ-ի աշխատակիցները
