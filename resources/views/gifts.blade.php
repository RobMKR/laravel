            Միջոցառմանը մասնակցելու ցանկություն ունեցող անձը, ով համապատասխանում է Միջոցառման Մասնակցի համար սույն Կանոններով սահմանված չափանիշներին, Միջոցառման ընդհանուր տևողության մեջ ընդգրկված 4 օրացուցային շաբաթվա ընթացքում Միջոցառման Կենտրոններից գնում է Ծխախոտային Ապրանքների առնվազն 7 Տուփ, տվյալ Տուփերի Փայլաթիթեղները դրանց Կտրոնների հետ միասին սույն Կանոններով սահմանված կարգով ներկայացնում է Միջոցառման Կազմակերպչին, կուտակում է շաբաթական առնվազն 7 Միավոր և ձեռք է բերում Միջոցառման համապատասխան Նվեր ստանալու հնարավորություն: 
Միջոցառման Կազմակերպիչը, Միջոցառման ողջ տևողության ընթացքում, Միջոցառման առաջին օրացուցային շաբաթվան հաջորդող շաբաթվանից սկսած, յուրաքանչյուր օրացուցային շաբաթվա ընթացքում, իրականացնում է Միջոցառման շրջանակներում ներկայացված Միավորների ամփոփում, համապատասխան տեղեկատվությունը տրամադրում է Միավորներ ներկայացրած համապատասխան անձանց, և համապատասխանաբար կազմակերպում և իրականացնում է Միջոցառման Մասնակիցների գրանցումը և/կամ համապատասխան Նվերների տրամադրումը՝ սույն Կանոններով սահմանված կարգով և պայմաններով: Միջոցառման ընդհանուր տևողության վերջին օրացուցային շաբաթվա արդյունքների ամփոփումն իրականացվում է Միջոցառման տևողության ավարտից հետո՝ դրան հաջորդող մեկ օրացուցային շաբաթվա ընթացքում: 
