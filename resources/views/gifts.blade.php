            <b>7.1.4</b> Միջոցառման Նվերները ստանալու համար Միջոցառման Մասնակիցը պարտավոր է Միջոցառման Պրոմոութերին ներկայացնել իր` անձը հաստատող փաստաթղթի բնօրինակը: <br>
            <b>7.1.5</b> Միջոցառման Նվերը ստանալիս Միջոցառման համապատասխան Մասնակիցը պարտավոր է ստորագրել դրանց ստացման վերաբերյալ համապատասխան փաստաթղթերը, եթե այդպիսիք առկա են Միջոցառման Պրոմոութերի մոտ:<br>
            <b>7.1.6</b> Միջոցառման Կազմակերպիչը Միջոցառման Կենտրոններում ապահովում է Նվերների առկայությունը՝ ըստ համապատասխան տեղաբաշխվածության: Միջոցառման Նվերների քանակը սահմանափակ է և սահմանված է սույն Կանոններով:<br>
            <b>7.1.7</b> Միջոցառման Պրոմոութերի մոտ Նվերների բացակայության դեպքում դրանք չեն տրամադրվում և Միջոցառման Կազմակերպիչը չի կրում դրա հետ կապված որևէ պարտավորություն:<br>

        </p>

        <p>
            <b>7.2 Նվերների տրամադրման և հանձնման լրացուցիչ դրույթները և պայմանները</b>
        </p>
        <p>
            <b>7.2.1</b> Եթե Միջոցառման Մասնակիցների միաժամանակ Միջոցառման Կենտրոններ ներկայանալու պատճառով Միջոցառման որևէ Մասնակցի համար անհրաժեշտություն առաջանա սպասելու՝ Նվեր ստանալու կամ գրանցվելու կամ Միջոցառման մասին տեղեկություններ ճշտելու և/կամ ստանալու համար, ապա Միջոցառման Կազմակերպիչը որևէ պատասխանատվություն չի կրում դրա համար, ինչպես նաև որևէ Միջոցառման Մասնակից չի կարող առավելություն ստանալ մյուսի հանդեպ՝ անկախ ցանկացած հանգամանքից: Միջոցառման Կենտրոններում գործողությունները, ներառյալ Միջոցառման Նվերների տրամադրումն իրականացվում են բացառապես ըստ հերթականության՝ առավել վաղ Միջոցառման Կենտրոն մոտեցած/մոտեցող անձին տալով առաջնահերթություն: <br>
            <b>7.2.2</b> Միջոցառման Նվերների կոնկրետ մոդելները (տեսակները), տեխնիկական և (կամ) բովանդակային առանձնահատկությունները որոշում է Միջոցառման Կազմակերպիչը: Միջոցառման վերաբերյալ տեղեկատվական նյութերում դրանց պատկերները և նկարագրությունն ունեն կողմնորոշիչ բնույթ, Միջոցառման Նվերները կարող են տարբերվել դրանցում պատկերվածներից: Միջոցառման Կազմակերպիչն իրավասու է փոփոխելու Միջոցառման Նվերների տեսակները և քանակները Միջոցառման ընթացքում, որի վերաբերյալ համապատասխան տեղեկատվությունը հասանելի կլինի Թեժ Գծի միջոցով:<br>
            <b>7.2.3</b> Միջոցառման ողջ ընթացքում Միջոցառման յուրաքանչյուր Մասնակից կարող է ստանալ առավելագույնը մեկ միատեսակ Միջոցառման Նվեր: <br>
            <b>7.2.4</b> Միջոցառման Նվերները հանձնվում են միայն համապատասխանաբար գրանցված Միջոցառման Մասնակիցներին: Արգելվում է Միջոցառման Նվերը հանձնել/փոխանցել ցանկացած երրորդ անձի: <br>
            <b>7.2.5</b> Միջոցառման որևէ Նվերի դիմաց համապատասխան գումարային հատուցում չի վճարվում: <br>
            <b>7.2.6</b> Եթե Միջոցառման որևէ Մասնակից, ով սույն Կանոնների համաձայն հանդիսանում է Միջոցառման ցանկացած Նվերի ստացող, չի ցանկանում ստանալ համապատասխան Միջոցառման Նվերը, ապա տվյալ անձը որևէ կերպ հարկադրված չէ ստանալու Միջոցառման Նվեր և ցանկության բացակայության դեպքում չի ստանում Միջոցառման համապատասխան Նվերը: Միջոցառման այդ Նվերները համարվում են չպահանջված և անցնում են Միջոցառման Կազմակերպչի տնօրինմանը, որը տնօրինում է նման Միջոցառման Նվերներն իր հայեցողությամբ, այդ թվում՝ ցանկության դեպքում տրամադրում է Միջոցառման այլ Մասնակցի:<br>
            <b>7.2.7</b> Միջոցառման Նվերը հանձնելու պահից սկսած` Միջոցառման Նվերի սեփականության իրավունքը, ինչպես նաև Միջոցառման Նվերի հետ կապված բոլոր ռիսկերը անցնում են Միջոցառման Մասնակցին՝ սույն Կանոնների պայմանները հաշվի առնելով: Միջոցառման Կազմակերպիչը պատասխանատու չէ Միջոցառման Նվերների որակի համար և չի տրամադրում Միջոցառման Նվերների համար որևէ երաշխիք, պատասխանատվություն չի կրում դրանց գործառնական կիրառելության, տեխնիկական հատկությունների և այլ առանձնահատկությունների համար (օրինակ՝ համատեղելիությունն ու կիրառելիությունն այլ սարքերի հետ):<br>
            <b>7.2.8</b> Միջոցառման Նվերների հետ կապված Հայաստանի Հանրապետության օրենսդրությամբ սահմանված հարկերը հաշվարկվում և վճարվում են` համաձայն Հայաստանի Հանրապետության օրենսդրության: <br>


        </p>
