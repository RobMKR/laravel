Միջոցառմանը մասնակցությունը հնարավոր է միայն փուլերի հերթականությամբ: Յուրաքանչյուր անձ կարող է մասնակցել Միջոցառմանը և համապատասխանաբար Միջոցառման բոլոր 4 փուլերին միայն 1 անգամ: Միջոցառմանը մասնակցությունը համապատասխան անձը կարող է սկսել միայն առաջին փուլից և շարունակել համապատասխանաբար՝ մասնակցելով Միջոցառման հետագա փուլերին այն քանակով, որքան հնարավորություն ունի մասնակցելու ելնելով Միջոցառման ժամկետային սահմանափակումից: Համապատասխան անձի կողմից Միջոցառման բոլոր փուլերին մասնակցությունը առանց բոլոր փուլերին հաջորդաբար մասնակցության հնարավոր չէ: Տվյալ անձի կողմից Միջոցառմանը մասնակցությունը ժամանակավոր դադարեցնելուց հետո վերսկսելու ցանկության դեպքում համապատասխան անձը կարող է շարունակել մասնակցությունը Միջոցառմանը այն փուլից, որից դադարեցրել է: 
