            <b>9.1 </b>Միջոցառման Նվերների յուրաքանչյուր տեսակի ընդհանուր քանակը սպառվելու պահից տվյալ տեսակի Նվերի մասով Միջոցառումը համարվում է ավարտված և դրանց հետ կապված Միջոցառման Կազմակերպիչը չունի որևէ այլ պարտավորություն:<br>
            <b>9.2</b> Միջոցառման Կազմակերպիչը պատասխանատվություն չի կրում Միջոցառման ընթացքում օգտագործվող հեռահաղորդակցության ծառայությունների, ներառյալ, ֆիքսված և/կամ բջջային կապի օպերատորի աշխատանքի, դրա որակի և/կամ դրա հետ կապված խնդիրների համար, որի պատճառով Միջոցառման Մասնակիցը չի կարողացել կապ հաստատել Միջոցառման շրջանակներում, չի ստացել ծանուցումներ, դրանք ստացել է ուշացումով կամ առնչվել է ցանկացած այլ խնդրի և/կամ անճշտության: <br>
            <b>9.3</b> Միջոցառման սույն Կանոնների համաձայն՝ որպես Միջոցառման Մասնակից գրանցվելիս անձը տրամադրում է իր անձնական տվյալները (անուն, ազգանուն, անձը հաստատող փաստաթղթի համար, այլ անհրաժեշտ տվյալներ): Եթե անձը որպես Միջոցառման Մասնակից գրանցվելիս օգտագործել է կեղծ տվյալներ և/կամ այլ կերպ անճշտություններ է մտցրել տվյալ գործընթացում, ապա Միջոցառման Կազմակերպիչն իրավասու է չհանձնել նրան Միջոցառման համապատասխան Նվերը, իսկ հանձնված լինելու պարագայում հետ պահանջել այն, հեռացնել տվյալ անձին Միջոցառման Մասնակիցների ցուցակից և անհրաժեշտության դեպքում, ըստ իր հայեցողության, համապատասխան պահանջ կամ բողոք ներկայացնել տվյալ անձի նկատմամբ, ինչպես նաև դիմել համապատասխան պետական մարմիններին տվյալ հարցի ուսումնասիրության նպատակով:<br>
            <b>9.4</b> Ընդունելով սույն Կանոնների պայմանները և գրանցվելով որպես Միջոցառման Մասնակից, անձը տալիս է իր համաձայնությունն առ այն, որ Միջոցառման Կազմակերպիչն իրավասու է օգտագործել Միջոցառման Մասնակցի կողմից տրամադրված և նրա Անձնական Պրոֆիլում առկա ամբողջ տեղեկատվությունը՝ Միջոցառման գործողության ողջ ժամանակահատվածում և դրանից հետո ցանկացած այլ մարքեթինգային ակտիվությունների և/կամ գովազդային նպատակներով: Միջոցառման Կազմակերպիչն իրավասու է օգտագործել այդ տեղեկատվությունը Միջոցառման Մասնակցի հետ առանց նախնական պայմանավորվածության և առանց որևէ փոխհատուցման: Սույն Կանոնների ընդունումը և հաստատումը Միջոցառման Մասնակցի կողմից համարվում է նրա կողմից Միջոցառման Կազմակերպչին նշված տեղեկություններն օգտագործելու տրամադրման պատշաճ համաձայնություն: Նշված տեղեկատվությունը կարող է օգտագործվել Միջոցառման Կազմակերպչի կողմից Հայաստանի Հանրապետության օրենսդրությանը համապատասխան: <br>
            <b>9.5</b> Մասնակցելով Միջոցառմանը և ընդունելով ու հաստատելով սույն Կանոնները՝ Միջոցառման Մասնակիցը տալիս է իր համաձայնությունը ստանալ Միջոցառման հետ կապված մարքեթինգային ցանկացած նյութեր, ներառյալ՝ գովազդային նյութեր, ստանալ Միջոցառմանն առնչվող այլ տեղեկատվություն, ինչպես նաև պարտավորվում է պատասխանել Միջոցառման Կազմակերպչի հարցումներին և ներկայացնել հավելյալ տվյալներ Միջոցառման Կազմակերպչին, եթե դրանք իրականացվում և պահանջվում են տվյալների ճշգրտման կամ Միջոցառման հետ կապված ցանկացած այլ նպատակներով:<br>
            <b>9.6</b> Եթե Միջոցառման Մասնակցի անձը հաստատող փաստաթղթի փաստաթղթային տվյալները չհամընկնեն այն տվյալների հետ, որոնք նա նշել է որպես Միջոցառման Մասնակից գրանցվելիս, ապա այդ անձը կորցնում է Միջոցառման Մասնակցի կարգավիճակը (բացառությամբ այն դեպքի, եթե տվյալ անձը փոփոխել է անձը հաստատող փաստաթուղթը օրենսդրությամբ սահմանված կարգով գրանցվելուց հետո և ներկայացնում է համապատասխան բավարար ապացույցներ) և իրավունք չունի ստանալու Միջոցառման Նվեր կամ այլ կերպ մասնակցելու Միջոցառմանը: <br>
            <b>9.7</b> Միջոցառման շրջանակներում տրամադրելով ցանկացած տեղեկատվություն՝ համապատախան անձը հաստատում է, որ իր կողմից տրամադրված տեղեկատվությունը (ներառյալ՝ լուսանկարներ և տեսանյութեր) և տվյալները չեն խախտում երրորդ անձանց իրավունքները: Միջոցառման Մասնակիցն անձամբ պատասխանատվություն է կրում Միջոցառման շրջանակներում ցանկացած նյութերի չարտոնված և անօրինական օգտագործման, ոչ ճշգրիտ կամ կեղծ տվյալներ ներկայացնելու և դրանց առաջացրած ցանկացած ուղղակի կամ անուղղակի հետևանքների համար: <br>
            <b>9.8</b> Միջոցառման շրջանակներում, Միջոցառման Կազմակերպիչն իրավասու է և կարող է հրապարակել Միջոցառման Մասնակիցների վերաբերյալ տեղեկատվություն՝ ներառյալ նրանց անունները և ազգանունները, ինչպես նաև իրավասու է տեղեկատվություն և նյութեր տարածել Միջոցառման Նվերներ ստացողների, դրանք հանձնելու գործընթացի և/կամ արարողության վերաբերյալ: Անձը, գրանցվելով որպես Միջոցառման Մասնակից, տալիս է իր համաձայնությունը նշված տեղեկությունների օգտագործման համար: Միջոցառման Կազմակերպիչն իր հայեցողությամբ ցանկացած ժամանակ իրավասու է օգտագործել և հրապարակել նշված տեղեկատվությունը և նյութերը՝ առանց Միջոցառման Մասնակիցներին նախապես տեղեկացնելու և առանց որևէ փոխհատուցման:  <br>
            <b>9.9</b> Միջոցառման Կազմակերպիչն իրավասու է թույլ չտալ Միջոցառմանը մասնակցել, ինչպես նաև իրավասու է Միջոցառման համապատասխան Նվերը չհանձնել այն անձանց, ովքեր փորձում են ուղղակիորեն կամ անուղղակիորեն ազդել  գրանցման համակարգի, ներառյալ դրա բոլոր բաղադրիչների, գործառնությունների վրա՝ կիրառելով Միջոցառման Մասնակցության Կանոններով չսահմանված միջոցներ կամ մեթոդներ: <br>
            <b>9.10</b> Միջոցառման Կազմակերպիչը պատասխանատվություն չի կրում Միջոցառմանը մասնակցության նպատակով Տուփերի ցանկացած ոչ իրավաչափ և/կամ ապօրինի ձեռքբերման, օգտագործման կամ փոխանցման համար. դրա համար պատասխանատվություն է կրում տվյալ գործողություններն իրականացրած/իրականացնող անձն անձամբ: <br>
            <b>9.11</b> Միջոցառման Կազմակերպիչն իրավասու է Միջոցառման ընթացքում ներկայացված Տուփերն ընդունելուց և համապատասխան գրանցումների իրականացումից հետո մասնակի կամ ամբողջությամբ ոչնչացնել դրանք՝ առանց ժամանակային կամ այլ ցանկացած սահմանափակման: <br>
            <b>9.12</b> Սույն Կանոնները մշակվել են Միջոցառման Կազմակերպչի կողմից Հայաստանի Հանրապետության օրենսդրությանը համապատախան՝ հաշվի առնելով Միջոցառման առանձնահատկությունները: Սույն Կանոնների և Հայաստանի Հանրապետության գործող օրենսդրության միջև հակասության դեպքում գերակայում են Հայաստանի Հանրապետության օրենսդրության դրույթները:  <br>
